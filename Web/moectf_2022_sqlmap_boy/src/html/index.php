<script>
	function login(){
		var username = $("#username").val();
		var password = $("#password").val();
		$.ajax({
			type:"POST",
			url:"login.php",
			data:{
				username:username,
				password:password
			},
			success:function(data){
				//json decode
				var data = JSON.parse(data);
				var code = data.code;
				var message = data.message;
				if(code == '0'){
					alert(message);
				}else{
					var redirect = data.redirect;
					window.location.href = redirect;
				}
			}
		});
	}
</script>
